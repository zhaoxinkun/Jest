// 创建一个构造函数
export default class Dog {
    // 构造器
    constructor() {
        this.number = 0
    }

    // 增加方法
    addOne() {
        this.number += 1
    }

    addTwo() {
        this.number += 2
    }

    // 减少方法
    minusOne() {
        this.number -= 1
    }

    minusTwo() {
        this.number -= 2
    }
}
